<template>
	<div :class="classes">
		<router-link v-if="hasRight && route != false" :to="{name: route, params: {id: (model) ? model.id : undefined}}" :target="target"><span :class="['fa', 'fa-'+this.realIcon]">&nbsp;</span><slot>{{ realLabel }}</slot></router-link>
		<a v-if="hasRight && href != false" :href="href" :target="target"><span :class="['fa', 'fa-'+this.realIcon]">&nbsp;</span><slot>{{ realLabel }}</slot></a>
	</div>
</template>

<style lang="less">
	@import '~uiStyle';
	.cp-link {
		padding: 0;
		a {
			color: white;
			padding: 6px 12px;
			line-height: 20px;
			height: 32px;
			display: block;
			&:hover {
				text-decoration: none;
			}
		}
	}
</style>

<script>
	module.exports = {
		props: {
			type: {
				required: false,
				type: String,
				default: 'primary'
			},
			size: {
				required: false,
				type: String,
				default: 'size-default'
			},
			href: {
				type: String,
				default: ''
			},
			icon: {
				required: false,
				type: String,
				default: ''
			},
			route: {
				type: String,
				default: ''
			},
			add: {
				type: Boolean,
				default: false
			},
			right: {
				default: undefined,
				required: false
			},
			target: {
				default: '_SELF'
			},
			model: {
				default: undefined
			}
		},
		computed: {
			realLabel: function() {
				if (this.add) {
					return 'Hinzuf√ºgen';
				}
			},
			realIcon: function() {
				if (this.add) {
					return 'plus';
				}

				return this.icon;
			},
			classes: function() {
				return ['cp-link', 'btn', 'btn-'+this.type, 'btn-'+this.size, 'btn-group-item'];
			},
			hasRight: function() {
				return this.$user == undefined || this.right == undefined || this.$user.hasRight(this.right);
			}
		}
	};
</script>
